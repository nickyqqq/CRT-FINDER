def fetch_subdomains(domain):
    """Fetch subdomains from crt.sh"""
    url = f"https://crt.sh/?q={domain}&output=json"

    try:
        print(f"[*] Fetching certificate data for {domain}...")
        response = requests.get(url, timeout=30)
        response.raise_for_status()

        data = response.json()
        return data
    except requests.exceptions.RequestException as e:
        print(f"[!] Error fetching data for {domain}: {e}")
        return None
    except json.JSONDecodeError:
        print(f"[!] Error parsing JSON response for {domain}")
        return None

def extract_subdomains(data):
    """Extract subdomains from certificate data"""
    if not data:
        return set()

    subdomains = set()
    # Regex pattern to match subdomains (word.word.word format)
    pattern = re.compile(r'(\w+\.\w+\.\w+)$')

    for entry in data:
        if 'name_value' in entry:
            names = entry['name_value'].split('\n')
            for name in names:
                name = name.strip()
                match = pattern.search(name)
                if match:
                    subdomains.add(match.group(1))

    return subdomains

def save_to_file(subdomains, output_file):
    """Save subdomains to file (anew behavior - append only unique)"""
    existing = set()

    # Read existing subdomains if file exists
    try:
        with open(output_file, 'r') as f:
            existing = set(line.strip() for line in f if line.strip())
    except FileNotFoundError:
        pass

    # Find new subdomains
    new_subdomains = subdomains - existing

    # Append new subdomains
    if new_subdomains:
        with open(output_file, 'a') as f:
            for subdomain in sorted(new_subdomains):
                f.write(subdomain + '\n')
        print(f"[+] Added {len(new_subdomains)} new subdomains to {output_file}")
    else:
        print(f"[*] No new subdomains found")

    return len(existing) + len(new_subdomains)

def read_domains_from_file(file_path):
    """Read domains from a text file"""
    try:
        with open(file_path, 'r') as f:
            domains = [line.strip() for line in f if line.strip()]
        return domains
    except FileNotFoundError:
        print(f"[!] Error: File '{file_path}' not found")
        sys.exit(1)
    except Exception as e:
        print(f"[!] Error reading file: {e}")
        sys.exit(1)

def process_domain(domain, output_file):
    """Process a single domain"""
    data = fetch_subdomains(domain)
    if data is None:
        return 0

    subdomains = extract_subdomains(data)

    if not subdomains:
        print(f"[!] No subdomains found for {domain}")
        return 0

    print(f"[*] Found {len(subdomains)} subdomains for {domain}")
    return subdomains

